function isValidText(text) {
  // Регулярное выражение, которое соответствует всем английским буквам, цифрам и большинству спецсимволов
  const regex = /^[a-zA-Z0-9 !@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]*$/;

  // Возвращает `true`, если текст содержит только разрешенные символы, иначе `false`
  return regex.test(text);
}

export default isValidText;
